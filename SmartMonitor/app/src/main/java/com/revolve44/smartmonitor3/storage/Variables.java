package com.revolve44.smartmonitor3.storage;

public class Variables {
    Float powerConsHour;
    Float rubHour;

    public void setPowerConsHour(Float powerConsHour) {
        this.powerConsHour = powerConsHour;
    }

    public void setRubHour(Float rubHour) {
        this.rubHour = rubHour;
    }

    public Float getPowerConsHour() {
        return powerConsHour;
    }

    public Float getRubHour() {
        return rubHour;
    }
}
